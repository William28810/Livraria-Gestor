    //Carregar tabela
    private void carregarHistorico(){
        DefaultTableModel modelo = (DefaultTableModel) tblHistorico.getModel();
        modelo.setNumRows(0);
        
        HistoricoDao dao = new HistoricoDao();
        
        List<Historico> lista = dao.listarHistorico();

        for (Historico h : lista) {
            modelo.addRow(new Object[]{
                h.getIdHistorico(),
                h.getTipoAcao(),
                h.getTabelaMovimentada(),
                h.getIdRegistroMovimento(),
                h.getFuncionario().getNome(),
                h.getDataHora()
            });
        }
        
    };
    
