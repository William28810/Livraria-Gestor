    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int linha = tblUsuarios.getSelectedRow();

        if (linha == -1) {
            JOptionPane.showMessageDialog(
                this,
                "Selecione um usuário para editar",
                "Atençăo",
                JOptionPane.WARNING_MESSAGE
            );
            return;
        }

        int id = (int) tblUsuarios.getValueAt(linha, 0);
        
        FuncionarioDao dao =new FuncionarioDao();
        Funcionario funcionario = dao.buscarFuncionarioPorId(id);
        if(funcionario == null){
            
            JOptionPane.showMessageDialog(this, "Erro ao carregar dados do usuário.");
            return;
        }
        

        TelaCadastroUsuario tela = new TelaCadastroUsuario(this);
        
        tela.setDadosUsuario(
                funcionario.getIdFuncionario(),
                funcionario.getNome(),
                funcionario.getCargo(),
                funcionario.getNivelPermissao(),
                funcionario.getLogin(),
                funcionario.getSenha()
            
        );
        
        tela.setVisible(true);
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int linhaSelecionada = tblUsuarios.getSelectedRow();

        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(
                this,
                "Selecione um usuário para excluir",
                "Atençăo",
                JOptionPane.WARNING_MESSAGE
            );
            return;
        }

        int idFuncionario = (int) tblUsuarios.getValueAt(linhaSelecionada, 0);

        int opcao = JOptionPane.showConfirmDialog(
            this,
            "Deseja realmente excluir este usuário?",
            "Confirmaçăo",
            JOptionPane.YES_NO_OPTION
        );

        if (opcao == JOptionPane.YES_OPTION) {

            FuncionarioDao funcionarioDao = new FuncionarioDao();
            funcionarioDao.excluirFuncionario(idFuncionario);
            
            JOptionPane.showMessageDialog(this, "Usuário excluído com sucesso!");
            carregarUsuario(); // atualiza tabela
             
        } else {
            
            JOptionPane.showMessageDialog(this, "Erro ao excluir usuário!");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    //Carregar tabela
    public void carregarUsuario(){
        DefaultTableModel modelo = (DefaultTableModel) tblUsuarios.getModel();
        modelo.setRowCount(0);
        
        FuncionarioDao funcionarioDao = new FuncionarioDao();
        Connection conexao = Conexao.conectar();
        
        List<Funcionario> lista = funcionarioDao.listarFuncionario();

        for (Funcionario funcionario : lista) {
            tableModel.addRow(new Object[]{
                funcionario.getIdFuncionario(),
                funcionario.getNome(),
                funcionario.getCargo(),
                funcionario.getNivelPermissao(),
                funcionario.getLogin()
            });
        }
        Conexao.desconectar(conexao);
    };

