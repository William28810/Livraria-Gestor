    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        String titulo = txtTitulo.getText().trim();
        String autor = txtAutor.getText().trim();
        String genero = txtGenero.getText().trim();
        String publicacao = txtPublicacao.getText().trim();
        String quantidade = txtQuantidade.getText().trim();
        String preco = txtPreco.getText().trim();
        String observacao = txtObservacao.getText();

        if (titulo.isEmpty() || autor.isEmpty() || genero.isEmpty() || publicacao.isEmpty() || quantidade.isEmpty() || preco.isEmpty()) {
            JOptionPane.showMessageDialog(
                this,
                "Preencha os campos obrigatórios.",
                "Atençăo",
                JOptionPane.WARNING_MESSAGE
            );
            return;
        }
        
        try{
            //Converter valore
            LocalDate dataPublicacao = LocalDate.parse(publicacao);
            int qtd = Integer.parseInt(quantidade);
            double valorPreco = Double.parseDouble(preco);
        
            Livro livro = new Livro(titulo, autor, genero, dataPublicacao, qtd, valorPreco, observacao);
            livro.setIdLivro(idLivro);
        
            LivroDao livroDao = new LivroDao();
        
        
            //NOVO LIVRO
            if(idLivro == 0){
            
            livroDao.cadastrarLivro(livro);
            JOptionPane.showMessageDialog(this, "Livro cadastrado com sucesso.");
            }       
            
            else{
            
            livroDao.editarLivro(livro);
            JOptionPane.showMessageDialog(this, "Livro atualizado com sucesso.");
            }
            
            if(painelLivros !=null){
            painelLivros.carregarLivro();
            
            }
            
            this.dispose();
        
        }catch (Exception e){
            
            JOptionPane.showMessageDialog(this, 
                    "Erro nos dados numricos´ou na data.\nVerifique os valores digitados.",
                    "ERRO",
                    JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

