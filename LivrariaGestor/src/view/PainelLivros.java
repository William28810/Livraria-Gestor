    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int linha = tblLivros.getSelectedRow();

        if (linha == -1) {
            JOptionPane.showMessageDialog(
                this,
                "Selecione um livro para editar",
                "Atençăo",
                JOptionPane.WARNING_MESSAGE
            );
            return;
        }

        int id = (int) tblLivros.getValueAt(linha, 0);
        
        LivroDao dao =new LivroDao();
        Livro livro = dao.buscarLivroPorId(id);
        if(livro == null){
            
            JOptionPane.showMessageDialog(this, "Erro ao carregar dados do livro.");
            return;
        };
        

        TelaCadastroLivro tela = new TelaCadastroLivro(this);
        
        tela.setDadosLivro(
            livro.getIdLivro(),
            livro.getTitulo(),
            livro.getAutor(),
            livro.getGenero(),
            livro.getDataPublicacao(),
            livro.getQuantidade(),
            livro.getPreco(),
            livro.getObservacao()
        );
        
        tela.setVisible(true);
        
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int linhaSelecionada = tblLivros.getSelectedRow();

        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(
                this,
                "Selecione um livro para excluir",
                "Atençăo",
                JOptionPane.WARNING_MESSAGE
            );
            return;
        }

        int idLivro = (int) tblLivros.getValueAt(linhaSelecionada, 0);

        int opcao = JOptionPane.showConfirmDialog(
            this,
            "Deseja realmente excluir este livro?",
            "Confirmaçăo",
            JOptionPane.YES_NO_OPTION
        );

        if (opcao == JOptionPane.YES_OPTION) {

            LivroDao livroDao = new LivroDao();
            livroDao.excluirLivro(idLivro);
            
            JOptionPane.showMessageDialog(this, "Livro excluído com sucesso!");
            carregarLivro(); // atualiza tabela
             
        } else {
            
            JOptionPane.showMessageDialog(this, "Erro ao excluir livro!");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed
    
    //Carregar tabela
    public void carregarLivro(){
        DefaultTableModel modelo = (DefaultTableModel) tblLivros.getModel();
        modelo.setRowCount(0);
        
        LivroDao livroDao = new LivroDao();
        Connection conexao = Conexao.conectar();
        
        List<Livro> lista = livroDao.listarLivros();

        for (Livro livro : lista) {
            tableModel.addRow(new Object[]{
                livro.getIdLivro(),
                livro.getTitulo(),
                livro.getAutor(),
                livro.getGenero(),
                livro.getQuantidade(),
                livro.getPreco(),
            });
        }
        
        Conexao.desconectar(conexao);

    }

