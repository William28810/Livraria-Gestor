    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int linha = tblClientes.getSelectedRow();

        if (linha != -1) {
        
            int id = (int) tblClientes.getValueAt(linha, 0);
            String nome = (String) tblClientes.getValueAt(linha, 1);
            String cpf = (String) tblClientes.getValueAt(linha, 2);
            String telefone = (String) tblClientes.getValueAt(linha, 3);
            String email = (String) tblClientes.getValueAt(linha, 4);
            String endereco = (String) tblClientes.getValueAt(linha, 5);
            
            TelaCadastroCliente tela = new TelaCadastroCliente(this);
            tela.setDadosCliente(id, nome, cpf, telefone, email, endereco);
            tela.setVisible(true);
        
        } else {
            
            JOptionPane.showMessageDialog(this, "Selecione um cliente para editar");
            return;
            
        }

        
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int linhaSelecionada = tblClientes.getSelectedRow();

        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(
                this,
                "Selecione um cliente para excluir",
                "Atençăo",
                JOptionPane.WARNING_MESSAGE
            );
            return;
        }

        int idCliente = (int) tblClientes.getValueAt(linhaSelecionada, 0);

        int opcao = JOptionPane.showConfirmDialog(
            this,
            "Deseja realmente excluir este cliente?",
            "Confirmaçăo",
            JOptionPane.YES_NO_OPTION
        );

        if (opcao == JOptionPane.YES_OPTION) {

            ClienteDao clienteDao = new ClienteDao();
            clienteDao.excluirCliente(idCliente);
            
            JOptionPane.showMessageDialog(this, "Cliente excluído com sucesso!");
            carregarClientes(); // atualiza tabela
             
        } else {
            
            JOptionPane.showMessageDialog(this, "Erro ao excluir cliente!");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void txtBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtBuscarActionPerformed
        String texto = txtBuscar.getText();
        
        ClienteDao clientedao = new ClienteDao();
        Conexao.conectar();

        List<Cliente> lista = clientedao.buscarCliente(texto);

        DefaultTableModel modelo = (DefaultTableModel) tblClientes.getModel();
        modelo.setRowCount(0); // limpa tabela

        for (Cliente cliente : lista) {
            modelo.addRow(new Object[]{
                cliente.getIdCliente(),
                cliente.getNome(),
                cliente.getCpf(),
                cliente.getTelefone(),
                cliente.getEmail(),
                cliente.getEndereco()
            });
        }

        Conexao.desconectar(conexao);
    }//GEN-LAST:event_txtBuscarActionPerformed
    
    //Carregar tabela
    public void carregarClientes(){
        DefaultTableModel modelo = (DefaultTableModel) tblClientes.getModel();
        modelo.setRowCount(0);
        
        ClienteDao clienteDao = new ClienteDao();
        Connection conexao = Conexao.conectar();
        
        List<Cliente> lista = clienteDao.listarClientes();
        
        for (Cliente c : lista){
            modelo.addRow(new Object[]{
                c.getIdCliente(),
                c.getNome(),
                c.getCpf(),
                c.getTelefone(),
                c.getEmail(),
                c.getEndereco()
            });
        }
        
        Conexao.desconectar(conexao);

    }

