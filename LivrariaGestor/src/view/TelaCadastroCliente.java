    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        String nome = txtNome.getText().trim();
        String cpf = txtCpf.getText().trim();
        String telefone = txtTelefone.getText().trim();
        String email = txtEmail.getText().trim();
        String endereco = txtEndereco.getText().trim();

        if (nome.isEmpty() || cpf.isEmpty() || telefone.isEmpty() || email.isEmpty() || endereco.isEmpty()) {
            JOptionPane.showMessageDialog(
                this,
                "Preencha todos os campos.",
                "Atençăo",
                JOptionPane.WARNING_MESSAGE
            );
            return;
        }
        
        Cliente cliente = new Cliente(nome, cpf, telefone, email, endereco);
        cliente.setIdCliente(idCliente);
        
        ClienteDao clienteDao = new ClienteDao();
        
        
        //NOVO CLIENTE
        if(idCliente == 0){
            
            if(clienteDao.cpfExiste(cpf, idCliente)){
                JOptionPane.showMessageDialog(this,"CPF já cadastrado!");
                return;
            }
            
            clienteDao.cadastrarCliente(cliente);
            JOptionPane.showMessageDialog(this, "Cliente cadastrado com sucesso.");
        }
        
        else{
            
            clienteDao.editarCliente(cliente);
            JOptionPane.showMessageDialog(this, "Cliente atualizado com sucesso.");
        }
        
        if(painelClientes !=null){
            painelClientes.carregarClientes();
        }
        
        this.dispose();
    }//GEN-LAST:event_btnSalvarActionPerformed

